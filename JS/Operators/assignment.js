// Assignment Operators;


// 1. Assignment =
// 2. Addition Assignment +=;
// 3. Substraction Assignment -=;
// 4. Multipliation Assignment *=;
// 5. Division /=
// 6. Modulus %=


var x = 12;

console.log(x);


// x = x + 10;

x += 10;

console.log(x);


// x = x -2;

x -= 2;

console.log(x);

// x = x * 2
x *= 2
console.log(x)
