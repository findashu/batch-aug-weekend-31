// Arithmetic Operators

// 1. Addition +
// 2. Subtract -
// 3. Multiplication *
//4. Division /
// 5. Modulus %
// 6. Increment ++
// 7. Decrement --


var addition = 10 + 5;

console.log(addition);

var substraction = 20 - 5;

var subs = 5 - 10;

console.log(substraction);

console.log(subs);


var multiplication = 11 * 11;

console.log(multiplication);

var division = 136 / 5;

console.log(division);


var mod = 136 % 5;

console.log(mod);


// Increment 

var x = 10;

console.log('Diff', x++);
console.log(x);

x++; // post-increment

console.log(x);

var y = 10;

++y // pre- increment

console.log(y);


// Decrement Operator

var w = 20;

console.log(w--); // 20

console.log(--w); // 18